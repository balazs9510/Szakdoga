        {
            var user = _context.Users
                .Include(u => u.Workouts)
                    .ThenInclude(w => w.ExerciseItems)
                        .ThenInclude(e  => e.Exercise)
                .SingleOrDefault(u => u.Id.Equals(id));
            if (user == null)
                return BadRequest();
            var workouts = user.Workouts;

            return Ok(workouts);
